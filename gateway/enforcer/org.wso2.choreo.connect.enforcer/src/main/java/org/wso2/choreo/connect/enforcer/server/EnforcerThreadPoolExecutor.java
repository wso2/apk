package org.wso2.choreo.connect.enforcer.server;

import org.wso2.choreo.connect.enforcer.grpc.ExtAuthService;

import java.util.concurrent.BlockingQueue;
import java.util.concurrent.ThreadFactory;
import java.util.concurrent.ThreadPoolExecutor;
import java.util.concurrent.TimeUnit;

/**
 * This is the thread pool executor class which runs the executor service threads. {@link ExtAuthService} runs using
 * this thread pool when requests are received.
 */
public class EnforcerThreadPoolExecutor extends ThreadPoolExecutor {

    public EnforcerThreadPoolExecutor(int corePoolSize, int maximumPoolSize, long keepAliveTime, TimeUnit unit,
            BlockingQueue<Runnable> workQueue, ThreadFactory threadFactory) {
        super(corePoolSize, maximumPoolSize, keepAliveTime, unit, workQueue, threadFactory);

    }
}
